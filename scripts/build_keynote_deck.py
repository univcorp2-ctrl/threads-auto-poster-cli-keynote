from __future__ import annotations

import argparse
from pathlib import Path

from pptx import Presentation
from pptx.dml.color import RGBColor
from pptx.enum.shapes import MSO_CONNECTOR, MSO_SHAPE
from pptx.enum.text import PP_ALIGN
from pptx.util import Inches, Pt

DARK = RGBColor(11, 16, 32)
INK = RGBColor(31, 41, 55)
MUTED = RGBColor(100, 116, 139)
BLUE = RGBColor(37, 99, 235)
CYAN = RGBColor(6, 182, 212)
GREEN = RGBColor(34, 197, 94)
AMBER = RGBColor(245, 158, 11)
RED = RGBColor(239, 68, 68)
WHITE = RGBColor(255, 255, 255)


def main() -> int:
    parser = argparse.ArgumentParser(description="Build a Keynote-compatible PPTX guide deck.")
    parser.add_argument("--output", type=Path, default=Path("dist/keynote/threads-auto-poster-keynote.pptx"))
    args = parser.parse_args()
    args.output.parent.mkdir(parents=True, exist_ok=True)

    prs = Presentation()
    prs.slide_width = Inches(16)
    prs.slide_height = Inches(9)

    add_cover(prs)
    add_architecture(prs)
    add_process(prs)
    add_program_map(prs)
    add_safety_model(prs)
    add_github_actions(prs)
    add_secret_setup(prs)
    add_operations(prs)

    prs.save(args.output)
    print(f"created {args.output}")
    return 0


