def add_process(prs: Presentation) -> None:
    slide = titled_slide(prs, "2. Threads投稿プロセス")
    steps = [("投稿文を読む", BLUE), ("500文字検証", CYAN), ("dry-run判定", GREEN), ("container作成", AMBER), ("threads_publish", RED), ("結果JSON保存", BLUE)]
    x = 0.7
    for label, color in steps:
        add_box(slide, label, x, 3.3, 2.1, 1.0, color, 17)
        if x < 12:
            arrow(slide, x + 2.15, 3.8, x + 2.75, 3.8)
        x += 2.55
    add_bullets(slide, ["API呼び出しは2段階: /threadsでコンテナIDを作り、/threads_publishで公開します。", "dry-runではThreads APIに接続せず、投稿予定と文字数だけをJSON化します。", "失敗時もartifactに構造化ログを残すので、CIから原因を追えます。"], 1.0, 5.45, 14.0, 1.8)
    add_footer(slide, "Process view")


