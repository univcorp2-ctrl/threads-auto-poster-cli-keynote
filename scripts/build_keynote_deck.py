def blank_slide(prs: Presentation):
    return prs.slides.add_slide(prs.slide_layouts[6])


def titled_slide(prs: Presentation, title: str):
    slide = blank_slide(prs)
    background(slide, WHITE)
    add_text(slide, title, 0.7, 0.45, 14.5, 0.5, 30, DARK, bold=True)
    line = slide.shapes.add_shape(MSO_SHAPE.RECTANGLE, Inches(0.7), Inches(1.15), Inches(14.6), Inches(0.04))
    line.fill.solid()
    line.fill.fore_color.rgb = RGBColor(226, 232, 240)
    line.line.fill.background()
    return slide


def background(slide, color: RGBColor) -> None:
    slide.background.fill.solid()
    slide.background.fill.fore_color.rgb = color


def add_box(slide, text: str, x: float, y: float, w: float, h: float, color: RGBColor, size: int = 18):
    shape = slide.shapes.add_shape(MSO_SHAPE.ROUNDED_RECTANGLE, Inches(x), Inches(y), Inches(w), Inches(h))
    shape.fill.solid()
    shape.fill.fore_color.rgb = color
    shape.line.color.rgb = color
    frame = shape.text_frame
    frame.clear()
    frame.word_wrap = True
    p = frame.paragraphs[0]
    p.alignment = PP_ALIGN.CENTER
    run = p.add_run()
    run.text = text
    run.font.bold = True
    run.font.size = Pt(size)
    run.font.color.rgb = WHITE
    return shape


def add_text(slide, text: str, x: float, y: float, w: float, h: float, size: int, color: RGBColor, *, bold: bool = False):
    box = slide.shapes.add_textbox(Inches(x), Inches(y), Inches(w), Inches(h))
    frame = box.text_frame
    frame.clear()
    frame.word_wrap = True
    p = frame.paragraphs[0]
    run = p.add_run()
    run.text = text
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.color.rgb = color
    return box


def add_bullets(slide, bullets: list[str], x: float, y: float, w: float, h: float) -> None:
    box = slide.shapes.add_textbox(Inches(x), Inches(y), Inches(w), Inches(h))
    frame = box.text_frame
    frame.clear()
    frame.word_wrap = True
    for idx, text in enumerate(bullets):
        p = frame.paragraphs[0] if idx == 0 else frame.add_paragraph()
        p.text = f"• {text}"
        p.font.size = Pt(18)
        p.font.color.rgb = INK
        p.space_after = Pt(8)


def arrow(slide, x1: float, y1: float, x2: float, y2: float) -> None:
    connector = slide.shapes.add_connector(MSO_CONNECTOR.STRAIGHT, Inches(x1), Inches(y1), Inches(x2), Inches(y2))
    connector.line.color.rgb = MUTED
    connector.line.width = Pt(2)


def add_footer(slide, text: str) -> None:
    add_text(slide, text, 0.75, 8.25, 14.5, 0.3, 11, MUTED)


